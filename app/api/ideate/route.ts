export async function POST(req: NextRequest) {
  const { hmwQuestion, insightCandidate, theme, caseId } = await req.json();

  if (!hmwQuestion) {
    return Response.json({ error: "HMWの問いを指定してください" }, { status: 400 });
  }

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (process.env.MOCK_MODE === "true" || !apiKey || apiKey === "your_api_key_here") {
    await new Promise((r) => setTimeout(r, 1200));
    const caseConfig = caseId ? getCaseById(caseId) : undefined;
    const ideasByHmw = caseConfig?.mockIdeasByHmw ?? TRAFFIC_MOCK_IDEAS_BY_HMW;
    const ideas = ideasByHmw[hmwQuestion] ?? MOCK_IDEAS_BY_HMW[hmwQuestion] ?? FALLBACK_IDEAS;
    return Response.json({ ideas });
  }

  const client = new Anthropic({ apiKey });
  const themeSection = theme?.trim() ? `\n## リサーチテーマ\n${theme}\n` : "";
  const prompt = `あなたはデザインシンキングのファシリテーターです。
${themeSection}
## 関連インサイト
${insightCandidate}

## How Might We（発散したい問い）
${hmwQuestion}

---

この「How Might We」の問いに対して、多様な視点からアイデアスターター（アイデアの種）を6〜8個生成してください。

ルール:
- 実現可能性は問わない（突拍子もないアイデアもOK）
- テクノロジー・環境設計・行動科学・コミュニティ・制度・教育など多様な視点をカバーする
- 各アイデアは短いタイトル（10字以内）と1文の説明のみ
- アイデアの種なので具体的な解決策でなくても良い、方向性・発想の糸口でOK

JSONのみ出力してください（説明文不要）：
[
  { "title": "タイトル", "description": "1文の説明" }
]`;

  const stream = new ReadableStream({
    async start(controller) {
      const encoder = new TextEncoder();
      try {
        const response = await client.messages.create({
          model: "claude-opus-4-6",
          max_tokens: 2048,
          thinking: { type: "adaptive" },
          messages: [{ role: "user", content: prompt }],
        });

        let jsonText = "";
        for (const block of response.content) {
          if (block.type === "text") jsonText += block.text;
        }

        let ideas: IdeaItem[] = [];
        try {
          const cleaned = jsonText
            .replace(/^```(?:json)?\n?/m, "")
            .replace(/\n?```$/m, "")
            .trim();
          ideas = JSON.parse(cleaned);
        } catch {
          controller.enqueue(
            encoder.encode(`data: ${JSON.stringify({ error: "AIの応答を解析できませんでした" })}\n\n`)
          );
          controller.close();
          return;
        }

        controller.enqueue(encoder.encode(`data: ${JSON.stringify({ ideas })}\n\n`));
        controller.close();
      } catch (err) {
        const message = err instanceof Error ? err.message : "不明なエラーが発生しました";
        controller.enqueue(encoder.encode(`data: ${JSON.stringify({ error: message })}\n\n`));
        controller.close();
      }
    },
  });

  return new Response(stream, {
    headers: {
      "Content-Type": "text/event-stream",
      "Cache-Control": "no-cache",
      Connection: "keep-alive",
    },
  });
}
